package com.vtalent.corejava.alekya1;

public class EndsWith {
	public static void main(String args[]){  
		String s1="java is OOPS";  
		System.out.println(s1.endsWith("S"));  
		System.out.println(s1.endsWith("OOPS")); 
		System.out.println(s1.endsWith("is"));
		System.out.println(s1.endsWith("OPS")); 
		System.out.println(s1.endsWith("oops")); 
		}

}
